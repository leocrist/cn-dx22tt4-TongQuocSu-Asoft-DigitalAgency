// Get related works
export function getRelatedWorks(slug: string, limit = 2) {
  const currentWork = works.find((work) => work.slug === slug);
  if (!currentWork) return [];

  return works
    .filter((work) => work.slug !== slug)
    .sort(() => 0.5 - Math.random())
    .slice(0, limit)
    .map((work) => ({
      title: work.title,
      image: work.image,
      category: work.category,
      slug: work.slug,
    }));
}

